<h2 id="产品简介" tabindex="-1"><a class="header-anchor" href="#产品简介" aria-hidden="true">#</a> 产品简介</h2>
<p>Block3D是专为3D设计师打造的零代码Web3D开发工具，用于帮助用户实现可交互的3D网页。其中，拼图是实现交互的主要工具，它具有直观、易用、模块化、结构化等优点，通过拖拽就能实现编程，极大地降低了用户的学习成本。</p>
<p>Block3D的底层是微软著名开源游戏引擎BabylonJS，Block3D全面继承了引擎强大、丰富的功能。</p>
<p>这是一款免费软件，用户可以用于商业项目开发，作者不会收取任何授权费用。</p>
<h2 id="为什么需要block3d" tabindex="-1"><a class="header-anchor" href="#为什么需要block3d" aria-hidden="true">#</a> 为什么需要Block3D？</h2>
